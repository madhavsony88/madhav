package loops;

public class forloop15_even {
	public static void main(String[] args) {
		int n=10;
		int even=0;

			for(int i=0;i<=n;i++) 
			{
				
			even=2*i;
			System.out.println(even);
		
		}
			int a=10;
			while(a>=10)
			{System.out.println(a);
			a--;
			}
			int p=20;
			do
			{
				System.out.println(p);
				p=p-2;
			}
			while(p>=20);
	}

}
